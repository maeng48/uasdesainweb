function goFamily() {
    document.getElementById("message").innerText = "Ini adalah keluarga saya";  // Mengubah teks di elemen dengan ID 'message'

    // Menambahkan gaya teks terpusat dengan JavaScript
    document.getElementById("message").style.textAlign = "center";  // Meratakan teks ke tengah
    document.getElementById("message").style.fontFamily = "Arial, sans-serif";  // Mengubah font
    document.getElementById("message").style.fontSize = "20px";  // Mengatur ukuran font
    document.getElementById("message").style.color = "#333";  // Mengatur warna font
}
